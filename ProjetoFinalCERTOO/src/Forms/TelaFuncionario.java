    public void carregarComboBoxDepartamento() throws SQLException{
        try {
            if (jComboFuncionario.getItemCount() > 0){
               jComboFuncionario.removeAllItems();
            }
        
          ArrayList<Funcionario> lista = srvFuc.getFuncionarioByLista();
          
          for (Funcionario u:lista){
            jComboFuncionario.addItem(u);
          }      
        
          this.limparTela();
          
        } catch (SQLException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
          };        
    
        
        
    }
    
