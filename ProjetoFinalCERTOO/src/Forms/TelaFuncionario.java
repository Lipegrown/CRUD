    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jtNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o nome do funcionario!");
            jtNome.requestFocus();
            return;
        }
        
        if (jtCpf.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o cpf!");
            jtCpf.requestFocus();
            return;
        }

        if (jtRg.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o RG!");
            jtRg.requestFocus();
            return;
        }
        
        if (jtData.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe a data de nascimento!");
            jtData.requestFocus();
            return;
        }
        
        if (jCodigoDepartamento.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o C√≥digo do Departamento!");
            jCodigoDepartamento.requestFocus();
            return;
        }
        
       try {
        if (jComboFuncionario.getSelectedIndex() >= 0){ //alterar
            Funcionario funcionario = (Funcionario) jComboFuncionario.getSelectedItem();
            
            funcionario.setNome(jtNome.getText());
            funcionario.setCpf(jtCpf.getText());
            funcionario.setRg(jtRg.getText());
            funcionario.setDataNascimento(formato.parse(jtData.getText()));
            funcionario.setIdDepartamento(Integer.parseInt(jCodigoDepartamento.getText()));
            srvFuc.update(funcionario);
           
        }
        else { //gravar
               Funcionario funcionario = new Funcionario(Integer.parseInt(jCodigoDepartamento.getText()),jtNome.getText(), jtCpf.getText(), jtRg.getText(), formato.parse(jtData.getText()));
               srvFuc.insert(funcionario);
               limparTela();
               
             }
          limparTela();
        this.atualizarListaFuncionario();
          
        } catch (SQLException ex) {
            
        } catch (ParseException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jComboFuncionarioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboFuncionarioItemStateChanged
        if (jComboFuncionario.getItemCount() <= 0){
            return;
        }
        
        if (jComboFuncionario.getSelectedIndex() < 0){
            return;
        } 
        
        Funcionario funcionario = (Funcionario) jComboFuncionario.getSelectedItem();
        jCodigoDepartamento.getText();
        jtNome.setText(funcionario.getNome());
        jtCpf.setText(funcionario.getCpf());
        jtRg.setText(funcionario.getRg());
        jtData.setText(formato.format(funcionario.getDataNascimento()));
        
        jLimpar.setEnabled(true);
    }//GEN-LAST:event_jComboFuncionarioItemStateChanged

