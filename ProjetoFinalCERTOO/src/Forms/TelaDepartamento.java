    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
         if (jComboBox1.getItemCount() <= 0){
            return;
        }
        
        if (jComboBox1.getSelectedIndex() < 0){
            return;
        } 
        
        Departamento departamento = (Departamento) jComboBox1.getSelectedItem();
        
        jNomeDepartamento.setText(departamento.getNomeDepartamento());
        
      
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void btSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSalvarMouseClicked
        if (jNomeDepartamento.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o Nome do Departamento!");
            jNomeDepartamento.requestFocus();
            return;
        }
        
       try {
        if (jComboBox1.getSelectedIndex() >= 0){ //alterar
            Departamento departamento = (Departamento) jComboBox1.getSelectedItem();
            departamento.setNomeDepartamento(jNomeDepartamento.getText());
            srvDep.update(departamento);
        }
        else { //gravar
               Departamento departamento = new Departamento(jNomeDepartamento.getText());
               srvDep.insert(departamento);
             }
         
        limparTela();
        this.atualizarListaDepartamento();
          
        } catch (SQLException ex) {
            
        }
    }//GEN-LAST:event_btSalvarMouseClicked

    private void btExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btExcluirMouseClicked
         if (jComboBox1.getSelectedIndex() < 0)
            return;
        
        //resp--> 0 = yes 1 = no 2 = cancel
        int resp = JOptionPane.showConfirmDialog(null, "Excluir registro?");
        if (resp == 0){
            try {
                srvDep.delete((Departamento) jComboBox1.getSelectedItem());
                
                this.atualizarListaDepartamento();
            } catch (SQLException ex) {
                Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btExcluirMouseClicked

