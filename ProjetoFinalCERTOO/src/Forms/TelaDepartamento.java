       public void carregarComboBoxDepartamento() throws SQLException{
        try {
            if (jComboBox1.getItemCount() > 0){
               jComboBox1.removeAllItems();
            }
        
          ArrayList<Departamento> lista = srvDep.getDepartamentoByLista();
          
          for (Departamento u:lista){
            jComboBox1.addItem(u);
          }      
        
          this.limparTela();
          
        } catch (SQLException ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
          };        
    
        
        
    }
    
    
